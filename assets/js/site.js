const supportedLanguages = Object.keys(translations);
const fallbackLanguage = "uz";

function getInitialLanguage() {
  const urlLanguage = new URLSearchParams(window.location.search).get("lang");
  if (supportedLanguages.includes(urlLanguage)) {
    return urlLanguage;
  }

  const savedLanguage = localStorage.getItem("madrimovClinicLanguage");
  if (supportedLanguages.includes(savedLanguage)) {
    return savedLanguage;
  }

  const browserLanguage = navigator.language.slice(0, 2).toLowerCase();
  if (supportedLanguages.includes(browserLanguage)) {
    return browserLanguage;
  }

  return fallbackLanguage;
}

function setLanguage(language) {
  const dictionary = translations[language] || translations[fallbackLanguage];

  document.documentElement.lang = language;
  document.title = dictionary.metaTitle;

  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.setAttribute("content", dictionary.metaDescription);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-lang-button]").forEach((button) => {
    const isActive = button.getAttribute("data-lang-button") === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("madrimovClinicLanguage", language);
}

document.querySelectorAll("[data-lang-button]").forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.getAttribute("data-lang-button"));
  });
});

const header = document.querySelector("[data-header]");
function updateHeaderState() {
  header?.classList.toggle("is-scrolled", window.scrollY > 12);
}

window.addEventListener("scroll", updateHeaderState, { passive: true });
updateHeaderState();

document.querySelector("[data-year]").textContent = new Date().getFullYear();
setLanguage(getInitialLanguage());
